import Foundation

enum StringStatic {
    static let returnKey: String = "\r"
    static let newLine: String = "\n"
    static let singleQuote: String = "\'"
    static let doubleQuote: String = "\""
    static let equal: String = "="
    static let lessThan: String = "<"
    static let largeThan: String = ">"
    static let slash: String = "/"
    static let space: String = " "
    static let escape: String = "\\"
    static let commentStart: String = "!--"
    static let commentEnd: String = "--"
    static let docType: String = "!DOCTYPE"
    static let empty: String = ""
}